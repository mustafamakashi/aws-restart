print("Python has three numeric types: int, float and complex")

#creating varibles

#integer datatype
myValue = 1

print(myValue)

print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))

#for floating values
myValue = 3.14

print(myValue)

print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))

#for complex datatype

myValue = 2+6j

print(myValue)

print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))

#boolean datatype

myValue = True

print(myValue)

print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))

myValue = False

print(myValue)

print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))